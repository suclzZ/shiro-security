package com.sucl.shirosecurity.entity;

public class Permission {

    private String permId;

    private String permCaption;

    private String permValue;

    public String getPermId() {
        return permId;
    }

    public void setPermId(String permId) {
        this.permId = permId;
    }

    public String getPermCaption() {
        return permCaption;
    }

    public void setPermCaption(String permCaption) {
        this.permCaption = permCaption;
    }

    public String getPermValue() {
        return permValue;
    }

    public void setPermValue(String permValue) {
        this.permValue = permValue;
    }
}
